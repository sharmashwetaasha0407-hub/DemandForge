def croston_sba(demand: np.ndarray, alpha: float = 0.1, n_periods: int = 8):
    """
    Syntetos-Boylan Approximation (SBA) — bias-corrected Croston method.

    Parameters
    ----------
    demand    : array-like, historical demand series
    alpha     : smoothing parameter (0 < alpha < 1). Lower = more stable.
    n_periods : number of future periods to forecast

    Returns
    -------
    dict with forecast values, fitted values, and components
    """
    demand = np.asarray(demand, dtype=float)
    n = len(demand)

    # Handle edge case: all zeros
    if demand.sum() == 0:
        return {
            'forecast': np.zeros(n_periods),
            'fitted': np.zeros(n),
            'z_smooth': np.zeros(n),
            'p_smooth': np.zeros(n),
            'alpha': alpha
        }

    # Initialization (use first non-zero demand)
    first_nz = demand[demand > 0]
    z = first_nz[0]        # initial smoothed demand size
    p = 1.0                # initial smoothed interval

    z_history = np.zeros(n)
    p_history = np.zeros(n)
    fitted = np.zeros(n)

    q = 1  # periods since last non-zero demand

    for t in range(n):
        if demand[t] > 0:
            # Update smoothed demand size
            z = alpha * demand[t] + (1 - alpha) * z
            # Update smoothed interval
            p = alpha * q + (1 - alpha) * p
            q = 1  # reset interval counter
        else:
            q += 1  # increment interval, no update to z or p

        z_history[t] = z
        p_history[t] = p

        # SBA forecast = bias-corrected Croston
        fitted[t] = (1 - alpha / 2) * (z / p)

    # Future forecast (constant, last fitted value)
    last_forecast = (1 - alpha / 2) * (z / p)
    forecast = np.full(n_periods, last_forecast)

    return {
        'forecast': forecast,
        'fitted': fitted,
        'z_smooth': z_history,
        'p_smooth': p_history,
        'alpha': alpha,
        'last_z': z,
        'last_p': p
    }


