def moving_average_forecast(demand, window=4, n_periods=8):
    """Simple moving average baseline."""
    ma = np.convolve(demand, np.ones(window)/window, mode='valid')
    last_val = ma[-1] if len(ma) > 0 else np.mean(demand)
    return np.full(n_periods, last_val)

