def walk_forward_eval(item_id, n_test=8, step=1):
    """Walk-forward validation: train on t, predict t+1, slide window."""
    series = weekly_agg[weekly_agg['item_id'] == item_id]['sales_quantity'].values
    n = len(series)
    train_end = n - n_test

    preds, actuals = [], []
    for t in range(train_end, n):
        train = series[:t]
        res = croston_sba(train, alpha=0.1, n_periods=1)
        preds.append(res['forecast'][0])
        actuals.append(series[t])

    mae = mean_absolute_error(actuals, preds)
    rmse = np.sqrt(mean_squared_error(actuals, preds))

    # Bias
    bias = np.mean(np.array(preds) - np.array(actuals))

    # MASE (Mean Absolute Scaled Error) — naive benchmark = last week demand
    naive_mae = mean_absolute_error(actuals[1:], actuals[:-1]) if len(actuals) > 1 else 1
    mase = mae / naive_mae if naive_mae > 0 else np.nan

    return {'item_id': item_id, 'MAE': mae, 'RMSE': rmse, 'Bias': bias, 'MASE': mase,
            'actuals': actuals, 'preds': preds}


