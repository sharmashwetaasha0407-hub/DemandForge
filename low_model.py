def optimize_alpha(demand: np.ndarray, alpha_range: np.ndarray = None, metric: str = 'MAE'):
    """
    Find the best alpha via walk-forward validation.
    Uses last 20% of data as validation window.
    """
    if alpha_range is None:
        alpha_range = np.arange(0.05, 0.55, 0.05)

    n = len(demand)
    val_start = int(n * 0.8)
    train = demand[:val_start]
    val = demand[val_start:]

    best_alpha = 0.1
    best_score = np.inf

    for a in alpha_range:
        result = croston_sba(train, alpha=a, n_periods=len(val))
        preds = result['forecast']

        if metric == 'MAE':
            score = mean_absolute_error(val, preds)
        elif metric == 'RMSE':
            score = np.sqrt(mean_squared_error(val, preds))

        if score < best_score:
            best_score = score
            best_alpha = a

    return best_alpha, best_score


