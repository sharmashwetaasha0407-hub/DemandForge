# ADI = Average Demand Interval | CV2 = Coefficient of Variation squared
# Croston classification matrix (Syntetos et al. 2005)

def classify_sku(series):
    """Classify SKU demand pattern: Smooth / Erratic / Intermittent / Lumpy"""
    non_zero = series[series > 0]
    if len(non_zero) == 0:
        return 'Dead', np.nan, np.nan

    # ADI: average interval between non-zero demands
    periods_with_demand = (series > 0).sum()
    total_periods = len(series)
    adi = total_periods / periods_with_demand if periods_with_demand > 0 else np.inf

    # CV2: squared coefficient of variation of demand sizes
    cv2 = (non_zero.std() / non_zero.mean()) ** 2 if non_zero.mean() > 0 else 0

    if adi < 1.32 and cv2 < 0.49:   return 'Smooth', adi, cv2
    elif adi < 1.32 and cv2 >= 0.49: return 'Erratic', adi, cv2
    elif adi >= 1.32 and cv2 < 0.49: return 'Intermittent', adi, cv2
    else:                             return 'Lumpy', adi, cv2

