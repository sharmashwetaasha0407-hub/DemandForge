import pandas as pd
import numpy as np

print("1. Generating State-Specific Historical Weather...")
# Load the calendar to get exact dates
df_calendar = pd.read_csv('calendar.csv')

# Create an array representing the days for our mathematical wave
days = np.arange(len(df_calendar))

# Define our State Climate Profiles
climate_profiles = {
    'CA': {'base_temp': 65, 'amplitude': 15, 'cold_thresh': 40, 'hot_thresh': 95},  # Mild & Stable
    'TX': {'base_temp': 75, 'amplitude': 25, 'cold_thresh': 32, 'hot_thresh': 100}, # Hot Summers
    'WI': {'base_temp': 45, 'amplitude': 40, 'cold_thresh': 15, 'hot_thresh': 90}   # Cold Winters
}

weather_dataframes = []

# Generate a unique weather timeline for each state
for state, profile in climate_profiles.items():
    # Mathematical Sine wave to simulate Summer/Winter cycles
    temp = profile['base_temp'] + profile['amplitude'] * np.sin(2 * np.pi * (days - 100) / 365)

    # Add random daily noise so it looks like real, unpredictable weather
    temp += np.random.normal(0, 5, len(df_calendar))

    # Flag extreme weather based on that specific state's tolerance
    extreme_flag = ((temp < profile['cold_thresh']) | (temp > profile['hot_thresh'])).astype(int)

    # Build a temporary dataframe for this state
    df_state = df_calendar[['wm_yr_wk']].copy()
    df_state['state_id'] = state
    df_state['avg_temp_F'] = np.round(temp, 1)
    df_state['extreme_weather'] = extreme_flag

    weather_dataframes.append(df_state)

# Combine all 3 states together
df_weather_daily = pd.concat(weather_dataframes)

# Roll up daily weather to weekly averages and max extreme flags
df_weather_weekly = df_weather_daily.groupby(['wm_yr_wk', 'state_id']).agg({
    'avg_temp_F': 'mean',
    'extreme_weather': 'max'
}).reset_index()

# Save the realistic weather file
df_weather_weekly.to_csv('historical_weather_state_specific.csv', index=False)
print("✅ State-Specific Weather Simulated and Saved!")

